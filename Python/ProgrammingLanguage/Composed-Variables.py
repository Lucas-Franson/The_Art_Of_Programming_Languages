# Load library
import numpy as np

# Create a vector as a row
vector_row = np.array([1, 2, 3])

print(vector_row)

# Create a vector as a column
vector_column = np.array([[1],
                          [2],
                          [3]])

print(vector_column)

# Create a matrix
matrix = np.array([[1, 2],
                   [1, 2],
                   [1, 2]])

print(matrix)